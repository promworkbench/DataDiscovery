package org.processmining.datadiscovery;

public class RuleDiscoveryException extends Exception {

	private static final long serialVersionUID = 8974014236336552630L;

	public RuleDiscoveryException() {
		super();
	}

	public RuleDiscoveryException(String message, Throwable cause) {
		super(message, cause);
	}

	public RuleDiscoveryException(String message) {
		super(message);
	}

	public RuleDiscoveryException(Throwable cause) {
		super(cause);
	}

}
